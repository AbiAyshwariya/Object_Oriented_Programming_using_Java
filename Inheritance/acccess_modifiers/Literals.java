class Literals{
    public static void main(String[] args){
        double num1=10e7;//Exponential Real Constants 
        int num2=056;//Octal
        int num3=0b0110010101;//Binary 
        int num4=10_00_000;//Numerical Literals with underscore
        System.out.println("Exponential Real Constants: "+num1);
        System.out.println("Octal Numbers: "+num2);
        System.out.println("Binary Numbers: "+num3);
        System.out.println("Numerical Literals:"+num4);
    }
}